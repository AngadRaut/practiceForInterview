package org.example.wrapperClasses;

public class Autoboxing {
    public static void main(String[] args) {
      // automatic conversion of primitives to wrapper objects
        int i = 100;
        Integer integer = i;
        System.out.println(integer);
        Integer i1 = 200 ;
        int a = i1;
        System.out.println(a);
    }
}